public class elseif {
    public static void main(String[] args ) {
        int guess =9;
        if (guess==2){
        System.out.println("You have guessed the number 2");
        } 
        else if(guess==3) {
            System.out.println(" You have guessed 3");
        }
        else if(guess==4){
        System.out.println("You have guessed 4");
        }
        else {
            System.out.println("You have guessed out of 2,3 & 4");
        }
    }
}
